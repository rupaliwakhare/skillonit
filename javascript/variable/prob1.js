// rules
// Variable names must begin with a letter, an underscore (_), or a dollar sign ($).

// Subsequent characters can be letters, numbers, underscores, or dollar signs.

// Variable names are case-sensitive (e.g., myVar and myvar are different variables).

// Reserved keywords (like function, class, return, etc.) cannot be used as variable names.

// Variable names cannot contain spaces.

// By convention, JavaScript variable names are written in camelCase (e.g., myVariableName).

// There is no limit to the length of a variable name.

